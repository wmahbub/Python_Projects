# Even and Odd Formulas

def is_even(a):
    """ Determines if the number (a) is even or not. """
    
    if a % 2 == 0:
        return True
    else:
        False
    

def is_odd(a):
    """ Determines if the number (a) is odd or not. """
    
    if a % 2 != 0:
        return True
    else:
        return False
    
